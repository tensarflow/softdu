    public void refreshJCBStudiengang(){
        
        Vector<Studiengang> studiengangContainer = new Vector<Studiengang>();
        studiengangContainer = sql.refreshStudiengangVector();
        
        jCBStudiengang.removeAllItems();
        
        for (int i = 0; i < studiengangContainer.size(); i++) {
            
            jCBStudiengang.addItem(studiengangContainer.get(i).studiengangID);
        }
    }


